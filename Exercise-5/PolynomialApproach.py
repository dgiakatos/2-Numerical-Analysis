import math
import random

def Lagrange(x, y):
    p = ""
    for i in range(len(x)):
        a = ""
        b = 1
        if i != 0:
            p = p + " + "
        for j in range(0, i):
            a = a + "(x - ({})) * ".format(x[j])
            b = b * (x[i] - x[j])
        for j in range(i + 1, len(x)):
            a = a + "(x - ({})) * ".format(x[j])
            b = b * (x[i] - x[j])
        p = p + "({}) * ".format(y[i]) + "(" + a + "(1) / ({}))".format(b)
    return p


